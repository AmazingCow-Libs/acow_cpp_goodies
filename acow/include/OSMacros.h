// BSDs
#define ACOW_OS_IS_BSD \
      BSD           || \
    __FreeBSD__     || \
    __NetBSD__      || \
    __OpenBSD__     || \
    __bsdi__        || \
    __DragonFly__

//------------------------------------------------------------------------------
// GNU/Linux
#define ACOW_OS_IS_GNU_LINUX \
    __gnu_linux__        ||  \
    __linux__

//------------------------------------------------------------------------------
// OSX
#define ACOW_OS_IS_OSX \
    __APPLE__       && \
    __MACH__

//------------------------------------------------------------------------------
// Windows
#define ACOW_OS_IS_WINDOWS \
    _WIN32

//------------------------------------------------------------------------------
// UNIX
#define ACOW_OS_IS_UNIX    \
    COREFS_IS_BSD       || \
    COREFS_IS_GNU_LINUX || \
    COREFS_IS_OSX
